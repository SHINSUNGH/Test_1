# Git은 버전 관리 시스템을 이용하여 파일들의 변경사항을 추적하고 관리하는 도구입니다. 다음은 Git의 간단한 명령어들입니다.

# 1. git init
# Git 저장소를 초기화합니다.

# 2. git add
# 변경된 파일들을 staging area에 추가합니다.

# 3. git commit
# 변경된 파일들을 커밋합니다.

# 4. git status
# 현재 저장소의 상태를 확인합니다.

# 5. git log
# 커밋 히스토리를 확인합니다.

# 6. git diff
# 변경된 파일들의 차이점을 확인합니다.

# 7. git branch
# 브랜치를 생성하거나 확인합니다.

# 8. git checkout
# 특정 브랜치나 커밋으로 이동합니다.

# 9. git merge
# 브랜치를 병합합니다.

# 10. git push
# 로컬 저장소의 변경사항을 원격 저장소로 전송합니다.

# 11. git pull
# 원격 저장소의 변경사항을 로컬 저장소로 가져옵니다.

# 12. git clone
# 원격 저장소의 내용을 로컬 컴퓨터로 복제합니다.

# 위와 같은 명령어들을 사용하여 Git을 효율적으로 활용할 수 있습니다.